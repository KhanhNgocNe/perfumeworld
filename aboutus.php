<h1 id="title">BARBIE DOLL – ABOUT US</h1>
<div class="body">
<p>Barbie is a fashion doll manufactured by the  ATN company and launched in March 1959.</p>
<br>

<p>Barbie is the figurehead of a brand of Mattel dolls and accessories, including other family members and collectible dolls.
     Barbie has been an important part of the toy fashion doll market for over sixty years, and has been the subject of numerous 
     controversies and lawsuits, often involving parodies of the doll and her lifestyle. Mattel has sold over a billion Barbie 
     dolls, making it the company's largest and most profitable line</p>

     <p>The Barbie doll brand has expanded into a media franchise, including a long-running series of animated films that began in 2001. 
     From 2002 to 2017, the films were aired regularly on the Nickelodeon cable channel.</p>

<p>Barbie and her boyfriend Ken have been described as two most popular dolls in the world.
     The doll has transformed the toy business in affluent communities worldwide by becoming a 
     vehicle for the sale of related merchandise (accessories, clothes, friends of Barbie, etc.)</p>

     <p> Barbie has become a cultural icon and has been given honors that are rare in the toy world. In 1974, a section of Times Square 
     in New York City was renamed Barbie Boulevard for a week. The Musée des Arts Décoratifs, Paris at the Louvre held a Barbie exhibit 
     in 2016. The exhibit featured 700 Barbie dolls over two floors as well as works by contemporary artists and documents (newspapers, 
     photos, video) that contextualize Barbie</p>
