a very selective distribution as they are produced in small quantities. The flacons are designed in a very special way as well…
You can often see dark shades. Nothing superfluous. Some perfumers engrave the name of the fragrance or decorate the flacon with different materials:
gemstones, wooden elements, leather etc. Quality is always the first priority. These perfumes are for customers who attach importance to something special.</p>

<p>Just visit our online shop, take a look at our recommendations for you and be enchanted by luxury. Should you need any assistance or be in search for inspiration,
the SF Perfume History team is happy to support. We assist you in the search for specific criteria, such as concentration, gender, occasion or specific ingredients.
We offer an assortment of the best niche fragrances for discerning customers. Should you not be able to find a specific fragrance, just write us an e-mail or call us.
We are at your disposal at any time.</p>
